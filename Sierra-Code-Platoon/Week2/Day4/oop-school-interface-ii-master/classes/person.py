class Person:
    def __init__(self, name, age, password, role):
        self.name = name
        self.age = age
        self.password = password
        self.role = role

    def get_name(self):
        return self.name
    
    def get_age(self):
        return self.age
    
    def get_password(self):
        return self.password
    
    def get_role(self):
        return self.role

    
    